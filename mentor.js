let uplodimage = document.getElementById("uplodimage");
let uplodeBtn = document.getElementById("uplodeBtn");
let size = document.getElementById("size");
function loadVideo(event) {
  const file = event.target.files[0];
  if (file) {
    const videoPlayer = document.getElementById("videoPlayer");
    videoPlayer.src = URL.createObjectURL(file);
    videoPlayer.style.display = "block";
    videoPlayer.style.maxWidth = "100%";
    videoPlayer.style.maxHeight = "100%";
    size.style.display = "none";
    uplodeBtn.style.display = "none";
    uplodimage.style.display = "none";
  }
}

document.getElementById("uplodimage").addEventListener("click", function () {
  document.getElementById("imageInput").click();
});

document
  .getElementById("imageInput")
  .addEventListener("change", function (event) {
    const file = event.target.files[0];
    if (file) {
      const imageUrl = URL.createObjectURL(file);
      document.getElementById("uplodimage").src = imageUrl;
      document.querySelector(".titel img").src = imageUrl;
      document.querySelector(".personal-image img").src = imageUrl;
    }
  });
